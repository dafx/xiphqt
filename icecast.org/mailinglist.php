<h3>Mailing List</h3>
<p>In additon to the web-based forum, we have 2 mailing lists devoted to icecast.  The first ([icecast]) is appropriate for general icecast usage questions.  The second ([icecast-dev]) is reserved for any developer related questions.</p>
<p>We have set up a mailing list <--> forum gateway which will (hopefully) replicate respective postings.  This means, that if you post to the forum, it will be automatically forwarded to the appropriate mailing list, and vice-versa.  This is a new feature for us, so please bear with it.. :)</p>

<p>To sign up for each mailing list, simple send an email to <font color="yellow"><b>icecast-request@xiph.org</b></font> or <font color="yellow"><b>icecast-dev-request@xiph.org</b></font> and include the text "subscribe" in the message.  Then simply follow the directions from there.  More details as well as access to the web-accessible archives of the mailing lists can be found at <a href="http://www.xiph.org/archives/">http://www.xiph.org/archives/</a> and <a href="http://www.xiph.org/icecast/list.html">http://www.xiph.org/icecast/list.html</a></p>
